public class Temp
{
	public Temp()
	{
        string name = "vitiligo";
         Connection newCon = new Connection();
        string query = String.Format("select DISTINCT SNP from {0} ", name);
        MySqlCommand command = new MySqlCommand(query, newCon.conn);
        MySqlDataAdapter dr = new MySqlDataAdapter(command);
        DataTable dt = new DataTable();
        dt.Clear();
        dr.Fill(dt);

        for(int i=0;i<dt.Rows.Count;i++)
        {
           string snp =  dt.Rows[i].ItemArray[0].ToString();
           MetaAnalaysis meta = new MetaAnalaysis(name, snp);
           meta.calculateHeterogenity();
           meta.CalculateCombineValue();
        }

        newCon.conn.Close();


	}
}